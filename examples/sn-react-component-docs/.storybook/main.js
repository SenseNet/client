const DocgenPlugin = require('./docgen-plugin')

module.exports = {
  webpackFinal: async config => {
    config.module.rules.push({
      test: /\.(ts|tsx)$/,
      use: [
        {
          loader: require.resolve('ts-loader'),
        },
      ],
    })
    config.resolve.extensions.push('.ts', '.tsx')
    config.plugins.push(new DocgenPlugin())
    return config
  },
}
